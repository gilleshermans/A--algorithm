def setStart(posX : int, posY: int, grid):
    grid[posY][posX] = 1
    return [posX, posY]

def setEnd(posX : int, posY : int, grid):
    grid[posY][posX] = 2
    return [posX, posY]

def createNodes(grid):
    columns = len(grid[0])
    rows = len(grid)
    entries = [[None for col in range(columns)] for row in range(rows)]
    for row in range(rows):
        for col in range(columns):
            e = Text(root, bg="White", width=2, height=1)
            e.grid(row=row, column=col)

            entries[row][col] = e
            
            if grid[row][col] == 1:
                e.config(bg="#0c6fa6")
                e.insert(INSERT, "START")
            elif grid[row][col] == 2:
                e.config(bg="#0c6fa6")
                e.insert(INSERT, "END")                
            elif grid[row][col] == 3:
                e.config(bg="Black")

    geoX = 25 * columns
    geoY = 25 * rows
    root.geometry("{0}x{1}".format(geoX , geoY))
    return entries


# Run the algorithm
def solve(openList, grid):
    path = []
    while openList != []:
        # Sort list based on lowest F-cost and H-cost
        openList.sort(key=operator.attrgetter("F", "H"))
        currentNode = openList[0]

        # Switch the current node to the closed list
        openList.remove(currentNode)
        closedList.append(currentNode)

        # If the goald has been reached
        # Backtrack to find the path
        if currentNode.pos == endNode:
            openList = []
            current = currentNode
            while current.pos != startNode:
                path.append(current)
                current = current.parent
            else:
                path.append(current)
            
        else:
            # Find neighbours
            children = currentNode.findNeighbours(grid)
            for child in children:
                child.parent = currentNode
                closedBreak = False
                openBreak = False
                for closed in closedList:
                    if child.pos == closed.pos:
                        closedBreak = True
                        break
                
                if not closedBreak:
                    # Calculate costs and add the child to the openList
                    child.G = math.sqrt(pow(child.x - child.parent.x, 2) + pow(child.y - child.parent.y, 2))
                    child.H = math.sqrt(pow(child.x - endNode[0], 2) + pow(child.y - endNode[1], 2))
                    child.F = child.G + child.H

                    for open in openList:
                        if child.pos == open.pos:
                            if child.G > open.G:
                                openBreak = True
                                break

                                
                    if not openBreak:
                        openList.append(child)
        
            # Update Graphics
            for a in openList:
                nodes[a.y][a.x].config(bg="Green")
            for b in closedList:
                nodes[b.y][b.x].config(bg="Red")
            root.update()

    for c in path:
        nodes[c.y][c.x].config(bg="#0c6fa6")
    
    return path

