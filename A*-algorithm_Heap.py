class Node:
    # initialize some values
    def __init__(self, x, y, parent):
        self.parent = parent
        
        self.x = x
        self.y = y
        self.pos = [x, y]
        self.G = 0.0
        self.H = 0.0
        self.F = 0.0

    # Get the adjacent nodes
    def findNeighbours(self, grid):
        self.neighbours = []
        for ix in [-1, 0, 1]:
            for iy in [-1, 0, 1]:
                if ix == 0 and iy == 0:
                    pass
                else:
                    try:
                        if grid[self.y + iy][self.x + ix] != 3 and self.y + iy >= 0 and self.x + ix >= 0:
                            self.neighbours.append(Node(self.x+ix, self.y+iy, self))
                    except IndexError:
                        print("IndexError, not on grid")

        return self.neighbours


