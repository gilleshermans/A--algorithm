def setStart(posX : int, posY: int, grid):
    grid[posY][posX] = 1
    return [posX, posY]

def setEnd(posX : int, posY : int, grid):
    grid[posY][posX] = 2
    return [posX, posY]

def createNodes(grid):
    columns = len(grid[0])
    rows = len(grid)
    entries = [[None for col in range(columns)] for row in range(rows)]
    for row in range(rows):
        for col in range(columns):
            e = Text(root, bg="White", width=2, height=1)
            e.grid(row=row, column=col)

            entries[row][col] = e
            
            if grid[row][col] == 1:
                e.config(bg="#0c6fa6")
                e.insert(INSERT, "START")
            elif grid[row][col] == 2:
                e.config(bg="#0c6fa6")
                e.insert(INSERT, "END")                
            elif grid[row][col] == 3:
                e.config(bg="Black")

    geoX = 50 * columns
    geoY = 50 * rows
    root.state('zoomed')
    return entries

def setup_maze(SX, SY, EX, EY, maze):
    startNode = setStart(SX, SY, maze)
    endNode = setEnd(EX, EY, maze)
    nodes = createNodes(maze)
    return startNode, endNode, nodes

def solve(grid, startNode, endNode):
    rootNode = Node(startNode[0], startNode[1], startNode)
    openList.list.append(rootNode)

    path = []
    while openList.list != []:
        startTime = time()
        # Sort list based on lowest F-cost and H-cost
        currentNode = openList.list[0]

        # Switch the current node to the closed list
        openList.delete()
        closedList.append(currentNode)

        # If the goald has been reached
        # Backtrack to find the path
        if currentNode.pos == endNode:
            endTime = time()
            print(endTime - startTime)
            openList.list = []
            current = currentNode
            while current.pos != startNode:
                path.append(current)
                current = current.parent
            else:
                path.append(current)
            
        else:
            # Find neighbours
            children = currentNode.findNeighbours(grid)

            for child in children:
                child.parent = currentNode
                closedBreak = False
                openBreak = False
                for closed in closedList:
                    if child.pos == closed.pos:
                        closedBreak = True
                        break
                
                if not closedBreak:
                    # Calculate costs and add the child to the openList
                    child.G = math.sqrt(pow(child.x - child.parent.x, 2) + pow(child.y - child.parent.y, 2)) + child.parent.G
                    child.H = math.sqrt(pow(child.x - endNode[0], 2) + pow(child.y - endNode[1], 2))
                    child.F = child.G + child.H

                    for open in openList.list:
                        if child.pos == open.pos:
                            if child.G > open.G:
                                openBreak = True
                                break
      
                    if not openBreak:
                        openList.insert(child)
                        
        
    # Update Graphics
    for a in openList.list:
        nodes[a.y][a.x].config(bg="Green")
    for b in closedList:
        nodes[b.y][b.x].config(bg="Red")
    root.update()

    for c in path:
        nodes[c.y][c.x].config(bg="#0c6fa6")
    
    return path


